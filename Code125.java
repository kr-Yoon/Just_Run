package JustRunJava;
	//java.lang.Math 클래스
	//반환값을 항시 확인할 것
public class Code125 {
	
public static void main(String[] args) {
	System.out.println("1."+Math.abs(-.35));	//절대값
	System.out.println("2. "+Math.ceil(-3.5));	//올림
	System.out.println("3. "+Math.floor(-3.5));	//내림
	System.out.println("4. "+Math.max(2.7, 6.7));	//큰값 반환
	System.out.println("5. "+Math.min(2.7, 6.1));	//작은값 반환
	System.out.println("6. "+Math.pow(2, 10));	//a의 b 제곱
	System.out.println("7. "+Math.round(3.7));	//반올림
	System.out.println("8. "+Math.sqrt(25));		//루트값 반환
	
}
}
