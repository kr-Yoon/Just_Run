package JustRunJava;

public class Code006 {

	
	public static void main(String[] args) {
		byte b = 126;
		System.out.println(b);
		b++;
		System.out.println(b);
		b++;
		System.out.println(b);
		
				
		byte a = -127;
		System.out.println(a);
		a--;
		System.out.println(a);
		a--;
		System.out.println(a);
			
		
	}
}
