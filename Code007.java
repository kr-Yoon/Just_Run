package JustRunJava;

public class Code007 {

	public static void main(String[] args) {
		short s = 32767;
		System.out.println(s);
		s++;
		System.out.println(s);
		s++;
		System.out.println(s);
		
		short v = -32767;
		System.out.println(v);
		v--;
		System.out.println(v);
		v--;
		System.out.println(v);
	

	}

}
